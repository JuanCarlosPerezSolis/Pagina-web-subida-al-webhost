		<h1>Principales reglas del futbol</h1>
		<p>Duración del partido ->De acuerdo con las reglas del fútbol cada partido se juega en dos periodos de 45 minutos. Los cambios de duración de cada periodo se toman antes del inicio del partido. Como hemos visto, entre ambos periodos se conserva un descanso entre 10 y 15 minutos.</p>
		<br>
		<br>
		<br>
		<p>Inicio y reanudación del juego ->Los capitanes del juego se reúnen con el árbitro para decidir de qué lado de la cancha jugarán. Lo sortean con una moneda, el ganador elige el lado.</p>
		<br>
		<br>
		<br>
		<p> Balón en juego o fuera de juego ->Las reglas del fútbol indican que el balón se considerará fuera de juego cuando traspase por completo los límites del terreno por aire o por tierra, o  bien; que el árbitro interrumpa el juego.</p>
		<br>
		<br>
		<br>
 		<p>Gol marcado ->Las reglas del fútbol son muy claras en denunciar un gol marcado: cuando un balón cruza completamente la línea de meta entre los postes verticales y por debajo del travesaño de la portería.</p>
 		<br>
		<br>
		<br>
 		<p>Fuera de juego ->l fuera de juego no es una infracción en sí y no se puede recibir sanción. El fuera de juego se da cuando un compañero le da un pase a otro y éste se encuentra: a)más cerca de la línea de meta contraria que el balón, b)más cerca de la línea de meta contraria que el penúltimo adversario, c) está a la mitad del campo del equipo contrario y d) el pase recibido no proviene de un saque de meta, banda o de esquina.</p>
 		<br>
		<br>
		<br>
		<br>
		<p>Faltas ->El tiro libre se da, al equipo contrario cuando el jugador cometa una infracción imprudente o de fuerza excesiva como: dar  una patada al adversario, poner o intentar poner una zancadilla al adversario, saltar sobre un adversario, cargar contra otro jugador, golpear o intentar golpear al otro, empujar. Asímismo, cuando se sujeta a un adversario, escupir, tocar el balón con las manos, etc.</p>
		<br>
		<br>
		<br>
		<br>
		<p>Tarjeta amarilla y roja ->La tarjeta amarilla se muestra a un jugador cuando es amonestado y la roja cuando se expulsa. El jugador se amonesta por: conducta antideportiva, desaprobar órdenes, infringir las reglas, retardar reanudación del juego, no respetar distancias, salir y entrar al terreno sin permiso.La expulsión se da cuando: hay conducta violenta, escupe a otro, impide con la mano un gol, emplea lenguaje o gesticula de manera ofensiva, recibe una 2da amonestación en el partido.</p>
		<br>
		<br>
		<br>
		<br>
		<p> Tiro libre ->Los tiros libres son directos e indirectos. Para el libre el balón debe tocar con el pie mientras el primero está libre y este jugador no puede volver a tocar el balón antes de que otro lo haya hecho. Los adversarios deben estar a unos 9 metros de distancia.</p>
		<br>
		<br>
		<br>
 		<p>Tiro penal o penalti ->Según las reglas del fútbol, el tiro penal se marca cuando un jugador comete infracción merecedora del cobro de un tiro libre directo dentro de su propia área penal. Lo ejecuatará un jugador del equipo contrario.</p>
 		<br>
 		<br>
		<br>
 		<p>Saques ->Existen, como indican las reglas del fútbol, saque de banda, saque de meta y saque de esquina sirven para reanudar el juego desde distintos lugares de la cancha como los nombres de los saques indican.</p>


	
